void OLED_SetCursor(uint8_t page, uint8_t col) {
    OLED_SendCommand(0xB0 + page);           // Set page address
    OLED_SendCommand(0x00 + (col & 0x0F));   // Lower column start address
    OLED_SendCommand(0x10 + ((col >> 4) & 0x0F)); // Upper column start address
}

void OLED_RangeClear(uint8_t page, uint8_t col) {
    OLED_SendCommand(0xB0 + page);           // Set page address
    OLED_SendCommand(0x00 + (col & 0x0F));   // Lower column start address
    OLED_SendCommand(0x10 + ((col >> 4) & 0x0F));// Upper column start address

    OLED_PrintString(" ");
}

void OLED_PrintChar(char ch) {
    if (ch < 32 || ch > 127) return;  // Ignore unsupported characters

    for (uint8_t i = 0; i < 6; i++) {
        OLED_SendData(font6x8[ch - 32][i]);
    }
}

void OLED_PrintString(char *str) {
    while (*str) {
        OLED_PrintChar(*str++);
    }
}

void OLED_Clear(void) {
    for (uint8_t page = 0; page < 8; page++) {
        OLED_SetCursor(page, 0);
        for (uint8_t col = 0; col < 128; col++) {
            OLED_SendData(0x00);
        }
    }
}

void OLED_rClear(uint8_t page, uint8_t col2) {
    for (uint8_t page2 = page; page2 < 8; page2++) {
        OLED_SetCursor(page2, 0);
        for (uint8_t col = 0; col < col2; col++) {
            OLED_SendData(0x00);
        }
    }
}

void oled_update_pixel(uint8_t page, uint8_t column, uint8_t data) {
    // Set the address
	 OLED_SendCommand(0xB0 | page);
    OLED_SendCommand(0x00 | (column & 0x0F));
    OLED_SendCommand(0x10 | ((column >> 4) & 0x0F));
    // Send the data
    OLED_SendData(data);
}

// Function to display an image on OLED
void OLED_DisplayImage(const uint8_t *image) {
    for (uint8_t page = 0; page < 8; page++) { // 8 pages for 64-pixel height (each page = 8 pixels)
        OLED_SendCommand(0xB0 + page);  // Set page address
        OLED_SendCommand(0x00);         // Set lower column address
        OLED_SendCommand(0x10);         // Set higher column address

        for (uint8_t col = 0; col < 128; col++) { // 128 columns for width
            OLED_SendData(image[page * 128 + col]);
        }
    }
}

void OLED_Init(void) {
    OLED_SendCommand(0xAE); // Display OFF
    OLED_SendCommand(0xD5); // Set display clock
    OLED_SendCommand(0x80);
    OLED_SendCommand(0xA8); // Set multiplex ratio
    OLED_SendCommand(0x3F);
    OLED_SendCommand(0xD3); // Set display offset
    OLED_SendCommand(0x00);
    OLED_SendCommand(0x40); // Set start line
    OLED_SendCommand(0x8D); // Charge pump
    OLED_SendCommand(0x14);
    OLED_SendCommand(0x20); // Memory mode
    OLED_SendCommand(0x00);
    OLED_SendCommand(0xA1); // Segment remap
    OLED_SendCommand(0xC8); // COM scan direction
    OLED_SendCommand(0xDA); // COM pins
    OLED_SendCommand(0x12);
    OLED_SendCommand(0x81); // Contrast
    OLED_SendCommand(0xCF);
    OLED_SendCommand(0xD9); // Precharge
    OLED_SendCommand(0xF1);
    OLED_SendCommand(0xDB); // VCOM detect
    OLED_SendCommand(0x40);
    OLED_SendCommand(0xA4); // Resume display
    OLED_SendCommand(0xA6); // Normal display
    OLED_SendCommand(0xAF); // Display ON
    OLED_Clear();
}

